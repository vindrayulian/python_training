# Mengulang String

# Program ini akan membuat komputer menulis kalimat "Ayo belajar!" sebanyak 3 kali.

# Baris pertama:
kata = "Ayo belajar!"
# Di sini kita membuat sebuah variabel bernama "kata".
# Variabel itu seperti kotak kecil untuk menyimpan sesuatu.
# Isi kotak ini adalah teks (tulisan) "Ayo belajar!"
# Tanda kutip (" ") menunjukkan bahwa isinya adalah teks, bukan angka.
# Jadi kalau kita bilang "kata", komputer tahu kalau isinya "Ayo belajar!"

# Baris kedua:
ulang = kata * 3
# Nah, ini bagian seru dan unik! ğŸ˜„
# Biasanya tanda bintang (*) dipakai untuk mengalikan angka.
# Tapi di sini, kita mengalikannya dengan teks!
# Maksudnya bukan "mengalikan angka", tapi "mengulang teks".
#
# Jadi "kata * 3" artinya:
# "Ulangi tulisan yang ada di variabel kata sebanyak 3 kali."
#
# Karena "kata" isinya "Ayo belajar!", maka hasilnya akan jadi seperti ini:
# "Ayo belajar!Ayo belajar!Ayo belajar!"
#
# Perhatikan ya â€” dia menempel tanpa spasi, karena di dalam teks aslinya tidak ada spasi di belakang tanda seru (!).
# Kalau kamu mau ada jarak antar kalimat, kamu bisa menambah spasi di akhir:
# kata = "Ayo belajar! "   <-- ada spasi sebelum tanda kutip tutup

# Baris ketiga:
print(ulang)
# "print" artinya: tampilkan hasil di layar.
# Jadi komputer akan menampilkan isi variabel "ulang",
# yang tadi berisi kalimat "Ayo belajar!" sebanyak 3 kali.

# Maka hasil di layar nanti adalah:
# Ayo belajar!Ayo belajar!Ayo belajar!
