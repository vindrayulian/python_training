# Di baris ini, kita membuat sebuah "kata ajaib" dan menyimpannya ke dalam kotak bernama 'kata'
# Jadi 'kata' ini seperti wadah yang berisi tulisan "Hebat!"
kata = "Hebat!"

# Nah, di baris ini kita ingin mengulang kata "Hebat!" sebanyak 5 kali
# Ibaratnya kalau kamu bilang "Hebat! Hebat! Hebat! Hebat! Hebat!" terus menerus
# Python bisa melakukan itu hanya dengan menulis kata * 5
# Tanda bintang (*) di sini artinya "ulang sebanyak..."
ulang = kata * 5

# Sekarang kita mau menampilkan hasilnya ke layar
# Jadi nanti di layar akan muncul tulisan "Hebat!Hebat!Hebat!Hebat!Hebat!"
# (Tanpa spasi karena kita tidak menambahkan jarak antar katanya)
print(ulang)
