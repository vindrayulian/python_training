# Di baris ini, kita membuat sebuah tulisan dan menyimpannya ke dalam kotak bernama 'kata'
# Tulisan itu adalah "Coding itu menyenangkan"
kata = "Coding itu menyenangkan"

# Nah, sekarang kita ingin mengubah semua huruf di dalam tulisan itu menjadi huruf besar
# Di Python, ada perintah ajaib bernama .upper()
# Kalau kita tambahkan di belakang tulisan seperti kata.upper(), maka semua hurufnya berubah jadi BESAR semua
# Ibaratnya seperti menekan tombol CAPS LOCK di keyboard untuk semua huruf
print(kata.upper())
