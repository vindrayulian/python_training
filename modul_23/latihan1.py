# Membuat fungsi untuk menggandakan angka

def kali_dua(angka):
    hasil = angka * 2 # Mengalikan angka dengan 2 atau proses penggandaan
    return hasil # Mengembalikan hasilnya

# Memanggil fungsi
angka_masuk = 6 # Masukan angka yang ingin kita gandakan
jawaban = kali_dua(angka_masuk)

print(f"Angka {angka_masuk} jika digandakan menjadi {jawaban}")