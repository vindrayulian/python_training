def tambah_angka(angka):
    hasil = angka + 10 # Menambahkan angka dengan 10
    return hasil # Mengembalikan hasilnya

# Memanggil fungsi
angka_masuk = 10 # Masukan angka yang ingin kita tambahkan 10
jawaban = tambah_angka(angka_masuk)

print(f"Angka {angka_masuk} jika ditambahkan dengan 10 menjadi {jawaban}")